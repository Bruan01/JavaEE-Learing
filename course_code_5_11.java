package course_code.five;

public class course_code_5_11 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-1));
        System.out.println(Math.sin(1));
        System.out.println(Math.sqrt(14));
        System.out.println(Math.cbrt(-1));//立方根
        System.out.println(Math.pow(3,3));//3*3*3
        System.out.println(Math.ceil(3.9));//最小整数
        System.out.println(Math.floor(-29.3));//最大整数
        System.out.println(Math.round(12.21));//四舍五入
        System.out.println(Math.random());//0-1
    }
}
